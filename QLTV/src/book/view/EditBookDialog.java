    private void setComBoBoxCategory(Book b) {
        var size = comboboxCategory.getItemCount();
        for (int i = 0; i < size; i++) {
            if (b.getCategory().compareTo(comboboxCategory.getItemAt(i)) == 0) {
                comboboxCategory.setSelectedIndex(i);
                break;
            }

        }

    }

