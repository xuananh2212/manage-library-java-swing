    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        var bookid = txtbookId.getText();
        var nameBook = txtNameBook.getText();
        var auThor = txtAuthor.getText();
        var category = comboboxCategory.getSelectedItem().toString();
        var price = txtPrice.getText();
        var producer = txtProducer.getText();
        int amount = (int) spinnerAmount.getValue();
        if (!bookid.isEmpty() && !nameBook.isEmpty() && !auThor.isEmpty() && !category.isEmpty()
                && !price.isEmpty() && !producer.isEmpty() && amount > 0) {
            try {
                book = new Book(bookid, nameBook, category, auThor ,producer , price, amount);
                 bf.editBook(book);
                 System.out.println(bf.edit);
               JOptionPane.showMessageDialog(rootPane, "sửa thành công");
               this.dispose();
            } catch (InValidPriceException ex) {
                JOptionPane.showMessageDialog(rootPane, "giá sách không hợp lệ!");
            } catch (InValidAuthorException ex) {
               JOptionPane.showMessageDialog(rootPane, "tên không hợp lệ");
            }
           
            

        }else{
        JOptionPane.showMessageDialog(rootPane, "thông tin không hợp lệ");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

