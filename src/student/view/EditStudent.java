    private void comboBoxMajorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboBoxMajorActionPerformed
        var studentId = txtStudentId.getText();
        var name = studentId.split("(AT17|CT05|DT04)");
        int id = Integer.parseInt(name[1]);
        if (comboBoxMajor.getSelectedIndex() == 0) {
            txtStudentId.setText("CT05" + id);

        } else if (comboBoxMajor.getSelectedIndex() == 1) {
            txtStudentId.setText("AT17" + id);

        } else if (comboBoxMajor.getSelectedIndex() == 2) {
            txtStudentId.setText("DT04" + id);

        }


    }//GEN-LAST:event_comboBoxMajorActionPerformed

