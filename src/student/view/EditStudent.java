    private void setComBoBoxCategory(Student s) {
        var size = comboBoxMajor.getItemCount();
        for (int i = 0; i < size; i++) {
            if (s.getMajor().compareTo(comboBoxMajor.getItemAt(i)) == 0) {
                comboBoxMajor.setSelectedIndex(i);
                break;
            }
        }

    }

